public class Objective7Lab4 {
  public static void main(String[] args) {

    //Initialize Variables
    int sum = 0, counter = 1;

    //While Loop
    while(counter <= 20) {

      //Calculation
      sum += counter;

      //Increment
      counter = counter + 1;

    }

    //Output - will display Sum Total once
    System.out.println(sum);

  }
}
